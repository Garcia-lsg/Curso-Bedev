// Função Simples:

function calculaDoisNumeros (numero1, numero2){
    console.log(numero1 + numero2)
}

calculaDoisNumeros(10,20)
calculaDoisNumeros(100,200)
calculaDoisNumeros(50,90)
calculaDoisNumeros(75,96)
calculaDoisNumeros(47,29)



// Arrow Functions:

const calculaTresNumeros = (numero1, numero2, numero3) => {
    console.log(numero1 + numero2 + numero3)
}

calculaTresNumeros(10,20,50)
calculaTresNumeros(50,90,20)
calculaTresNumeros(100,200,52)
calculaTresNumeros(75,96,51)
calculaTresNumeros(47,29,63)

